export function resolveAppLanguage(language: AppLanguage = "system") {
  if (language === "ru" || language === "en") {
    return language;
  }

  if (typeof navigator !== "undefined") {
    return navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en";
  }

  return "en";
}

export function applyAppLanguage(language: AppLanguage = "system") {
  const resolvedLanguage = resolveAppLanguage(language);
  window.localStorage.setItem("contextforge.language", language);
  return i18n.changeLanguage(resolvedLanguage);
}

void i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: "en",
    supportedLngs: ["en", "ru"],
    load: "languageOnly",
    detection: {
      order: ["localStorage", "navigator"],
      lookupLocalStorage: "contextforge.language",
      caches: []
    },
    interpolation: {
      escapeValue: false
    }
  });

export default i18n;
